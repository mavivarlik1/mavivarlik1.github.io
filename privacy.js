window.updatePrivacyUI = function(lang) {
    const data = privacyData[lang] || privacyData['tr'];
    
    const dateEl = document.getElementById('privacyDate');
    const bodyEl = document.getElementById('privacyBody');
    
    if (dateEl) dateEl.innerText = data.date;
    if (bodyEl) bodyEl.innerHTML = data.body;
};

// Modül ilk yüklendiğinde hafızadaki aktif dile göre arayüzü doldur
document.addEventListener("DOMContentLoaded", () => {
    const activeLang = localStorage.getItem('corebase_lang') || 'tr';
    if (window.location.hash === '#privacy') {
        window.updatePrivacyUI(activeLang);
    }
});
